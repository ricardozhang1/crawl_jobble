import MySQLdb
import MySQLdb.cursors
from twisted.enterprise import adbapi

class PicturespiderPipeline(object):
    def process_item(self, item, spider):
        return item


class MysqlTwistedPipline(object):
    def __init__(self, dbpool):
        self.dbpool = dbpool

    @classmethod
    def from_settings(cls, settings):
        dbparms = dict(
            host = settings["MYSQL_HOST"],
            db = settings["MYSQL_DBNAME"],
            user = settings["MYSQL_USER"],
            passwd = settings["MYSQL_PASSWORD"],
            charset='utf8',
            cursorclass=MySQLdb.cursors.DictCursor,
            use_unicode=True,
        )
        dbpool = adbapi.ConnectionPool("MySQLdb", **dbparms)
        return cls(dbpool)

    def process_item(self, item, spider):
        #使用twisted将mysql插入变成异步执行
        query = self.dbpool.runInteraction(self.do_insert, item)
        query.addErrback(self.handle_error, item, spider) #处理异常

    def handle_error(self, failure, item, spider):
        #处理异步插入的异常
        print (failure)

    def do_insert(self, cursor, item):
        #执行具体的插入
        insert_sql = """
            insert into text_05(title, url, url_object_id, create_time, like_num, comment_num, front_image_url) VALUES (%s, %s, %s, %s,%s, %s, %s)
        """
        param =(item["title"], item["url"], item["url_object_id"], item["create_time"], item["like_num"], item["comment_num"],item["front_image_url"])
        cursor.execute(insert_sql, param)
